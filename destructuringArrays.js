//destructuringArrays.js unpacking values

let arr = ["i ","like","butter","cookies"]
//  we can extract values of array using indexing as below 
let a = arr[0]
let b = arr[1]
console.log(a,b)
// Another way is using array variables as below 
let [p,q,r,s] = arr
console.log(p + q + r+s)
let [x,y] = arr
console.log(x+y)
